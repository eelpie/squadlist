package uk.co.squadlist.web.model;

public class Instance {
	
	private String id;
	private String name;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	@Override
	public String toString() {
		return "Instance [id=" + id + ", name=" + name + "]";
	}
	
}
